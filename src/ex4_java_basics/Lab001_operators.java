package ex4_java_basics;

public class Lab001_operators {
    static void main(String[] args) {
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);
        boolean b = false;
        boolean c = a && b;
        System.out.println(c);
        boolean d = a || b;
        System.out.println(d);
        boolean e = !(a);
        System.out.println(e);
    }
}
